package practica3;

public class AreaEmpresa {
	private String id;
	private int tar;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getTar() {
		return tar;
	}
	public void setTar(int tar) {
		this.tar = tar;
	}
	public AreaEmpresa(String id, int tar) {
		super();
		this.id = id;
		this.tar = tar;
	}
	
	
	
}
