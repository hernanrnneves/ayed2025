package practica2;

public class SumaDif {
	private int suma, dif;

	public SumaDif(int suma, int dif) {
		super();
		this.suma = suma;
		this.dif = dif;
	}

	public int getSuma() {
		return suma;
	}

	public void setSuma(int suma) {
		this.suma = suma;
	}

	public int getDif() {
		return dif;
	}

	public void setDif(int dif) {
		this.dif = dif;
	}
	
	
	
	
}
